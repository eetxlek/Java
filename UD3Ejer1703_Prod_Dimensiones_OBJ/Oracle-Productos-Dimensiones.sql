-- Crear Tipo 
CREATE OR REPLACE TYPE DIMENSIONES_T AS OBJECT  (
  LARGO     NUMBER(3),
  ANCHO     NUMBER(3),
  ESPESOR 	NUMBER(3)
);
/

-- Crear la tabla tomando como estructura un TYPE 
CREATE TABLE PRODUCTOS (
  ART	 		VARCHAR2(3) PRIMARY KEY,
  DESCRIPCION	VARCHAR2(30),
  CATEGORIA		VARCHAR2(4),
  DIMENSIONES	DIMENSIONES_T,
  PRECIO		NUMBER(10,2)  
);

 
